/// WGS-84 楕円体の長半径（赤道半径）[m]
///
/// 地球を近似する WGS-84 楕円体における赤道方向の半径を表す。
/// ECEF 変換や測地計算の基準となる定数。
pub const WGS84_A: f64 = 6_378_137.0;

/// WGS-84 楕円体の逆扁平率（1 / f）
///
/// 扁平率 `f` は楕円体のつぶれ具合を表す無次元量であり、
/// 本定数はその逆数を表す。
pub const WGS84_INV_F: f64 = 298.257_223_563;

/// WGS-84 楕円体の扁平率 `f`
///
/// `f = 1 / WGS84_INV_F`
///
/// 楕円体の短半径と長半径の比を定義するために用いる。
pub const WGS84_F: f64 = 1.0 / WGS84_INV_F;

/// WGS-84 楕円体の短半径（極半径）[m]
///
/// `b = a * (1 - f)` により定義され、
/// 地球の自転軸方向の半径を表す。
pub const WGS84_B: f64 = WGS84_A * (1.0 - WGS84_F);

/// WGS-84 楕円体の第一離心率の二乗（e²）
///
/// `e² = 1 - (b² / a²)`
///
pub const WGS84_E2: f64 = 1.0 - (WGS84_B * WGS84_B) / (WGS84_A * WGS84_A);
